// GetRandomName Getting random two word name for projects.
func GetRandomName() string {
	randomAdjIndex, _ := rand.Int(rand.Reader, big.NewInt(int64(len(adjectives))))
	randomNounIndex, _ := rand.Int(rand.Reader, big.NewInt(int64(len(nouns))))
	name := fmt.Sprintf("%s_%s", adjectives[randomAdjIndex.Int64()], nouns[randomNounIndex.Int64()])
	return name
}
